print('Olá Mundo')
# Utilizar Aspas escreve uma mensagem. Todo conteúdo dentro de aspas é considerado uma mensagem.
print(7+4)
# Utilizar numeros e operadores dentro de um Print retorna o resultado da operação.
print('7'+'4')
# Se colocar os numeros entre aspas, ira ocorrer a concatenação dos numeros ao invés da soma.
print('Olá', 5)
# é possível concatenar letras/palavras dentro de aspas com numeros fora de aspas, separando as mensagens a serem unidas, com uma virgula.
# Variáveis:
nome = 'Leandro'
idade = 29
peso = 111.5
# É possível atribuir valores para essas varaiaveis utilizando o operador de igual.
print(nome, idade, peso)
# Ao realizar um print utilizando o nome das variáveis será retornado os valores atribuidos a elas.
